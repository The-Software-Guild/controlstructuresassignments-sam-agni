
public class SwitchCaseTest {

	public static void main(String[] args) {
		char c = 'a';
		switch (c) {
			case 'a': {
				System.out.println("character is a vowel");
				break;
			}
			case 'e': {
				System.out.println("character is a vowel");
				break;
			}
			case 'i': {
				System.out.println("character is a vowel");
				break;
			}
			case 'o': {
				System.out.println("character is a vowel");
				break;
			}
			case 'u': {
				System.out.println("character is a vowel");
				break;
			}
			default: {
				System.out.println("character is not a vowel");
			}
		}

	}

}
